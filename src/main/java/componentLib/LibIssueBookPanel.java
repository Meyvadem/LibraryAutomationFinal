    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showConfirmDialog(this, "Please Select A Costumer!", "Costumer Selectin is Null", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Member memb = (Member) jComboBox1.getSelectedItem();

        String sqlMember = "SELECT * FROM member WHERE member_id=?";
        try ( PreparedStatement psMember = con.prepareStatement(sqlMember)) {
            psMember.setInt(1, memb.getMemberID());
            ResultSet rsMember = psMember.executeQuery();

            while (rsMember.next()) {
                String sqlBook = "SELECT * FROM book WHERE book_id=?";
                try ( PreparedStatement psBook = con.prepareStatement(sqlBook)) {
                    psBook.setInt(1, Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
                    ResultSet rsBook = psBook.executeQuery();
                    while (rsBook.next()) {
                        System.out.println(rsMember.getInt("member_id"));
                        String sqlInsert = "INSERT INTO issue_book"
                                + "(issue_book_id, title,author_name,category)"
                                + "VALUES (?,?,?,?)";
                        try ( PreparedStatement psInsert = con.prepareStatement(sqlInsert)) {
                            psInsert.setInt(1, rsBook.getInt("book_id"));
                            psInsert.setString(2, rsBook.getString("title"));
                            psInsert.setString(3, rsBook.getString("author_name"));
                            psInsert.setString(4, rsBook.getString("category"));
                            psInsert.executeUpdate();

                            Vector newData = new Vector();
                            newData.add(jTable1.getValueAt(jTable1.getSelectedRow(), 0));
                            newData.add(jTable1.getValueAt(jTable1.getSelectedRow(), 1));
                            newData.add(jTable1.getValueAt(jTable1.getSelectedRow(), 2));
                            newData.add(jTable1.getValueAt(jTable1.getSelectedRow(), 3));
                            newData.add(jTable1.getValueAt(jTable1.getSelectedRow(), 4));

                            String sqlUpdate = "UPDATE book "
                                    + "SET is_available=?,member_id=? "
                                    + "WHERE book_id=?";
                            try ( PreparedStatement psUpdate = con.prepareStatement(sqlUpdate)) {
                                psUpdate.setBoolean(1, false);
                                psUpdate.setInt(2, rsMember.getInt("member_id"));
                                psUpdate.setInt(3, Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
                                psUpdate.executeUpdate();

                            } catch (SQLException ex) {
                                Logger.getLogger(LibIssueBookPanel.class.getName()).log(Level.SEVERE, null, ex);
                            }
                            RentalBookTable.addRow(newData);
                            BookTable.removeRow(jTable1.getSelectedRow());

                        } catch (SQLException ex) {
                            Logger.getLogger(LibIssueBookPanel.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(LibIssueBookPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            }

        } catch (SQLException ex) {
            Logger.getLogger(LibIssueBookPanel.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnSaveActionPerformed


