    void fillBookTable() {
        String sql = "SELECT * FROM book WHERE is_available=?";
        try ( PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setBoolean(1, true);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                BookTable.addRow(new Object[]{rs.getInt("book_id"), rs.getString("title"), rs.getString("author_name"),
                    rs.getString("category"), rs.getInt("price")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(LibIssueBookPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void fillMemberCombo() {
        try {
            Statement s = con.createStatement();
            String sql = "SELECT * FROM Member";
            ResultSet rs = s.executeQuery(sql);
            while (rs.next()) {
                jComboBox1.addItem(new Member(rs.getInt("member_id"), rs.getString("first_name"),
                        rs.getString("last_name"), rs.getInt("password"), rs.getString("username")));
            }
        } catch (SQLException ex) {
            Logger.getLogger(LibIssueBookPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

